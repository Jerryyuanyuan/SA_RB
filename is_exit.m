function panding=is_exit(c,A)
 %-------------------------------------------------------------------------
 %  判定矩阵A中是否存在c向量
 %  
 %-------------------------------------------------------------------------
    [m,n]=size(A);
    res = zeros(m,1);
    for i = 1:n
        res = res + (c(i)==A(:,i));
    end
    panding = (sum(res==n)>0);
end